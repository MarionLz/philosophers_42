#include "../inc/philo.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	digit;

	sign = 1;
	digit = 0;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == 32)
		nptr++;
	if (*nptr == 43 || *nptr == 45)
	{
		if (*nptr == 45)
			sign *= -1;
		nptr++;
	}
	while (*nptr >= 48 && *nptr <= 57)
	{
		digit = (digit * 10) + *nptr - 48;
		nptr++;
	}
	return (digit * sign);
}
